package DeitelJavaProgrammingBook.ChapterTwo;
public class IdentifyAndCorrect {
    public static void main(String[] args) {

        int c = 0;

        if (c < 7) {
            System.out.println("c is less than 7");
        }
        if (c >= 7) {
            System.out.println("c is equal to or greater than 7");
        }
    }

}